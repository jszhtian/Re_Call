//━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// 描画
// ★回転ありの場合
	if(r != 0){
		while(1){
			if(p1_cnt == 0){
				if(p1_flag){break;}
				p1 = lb_p[1];
				p1_cnt = lb_cnt[1];
				p1 += 2;
				p1_cnt--;
				if(p1_cnt == 0){break;}
				p1_flag = 1;
			}
			if(p2_cnt == 0){
				if(p2_flag){break;}
				p2 = lb_p[2] + ((lb_cnt[2] - 1) << 1);
				p2_cnt = lb_cnt[2];
				p2 -= 2;
				p2_cnt--;
				if(p2_cnt == 0){break;}
				p2_flag = 1;
			}

			y = *(p1 + 1);
			if(y != *(p2 + 1)){break;}

			if(y>=dey1 && y<=dey2){
				x1 = *p1;
				x2 = *p2;

				if(x1 > x2){
					tmp = x1;
					x1 = x2;
					x2 = tmp;
				}

				if(x1<=dex2 && x2>=dex1){

				// (dcx, dcy) を中心とした座標を元に戻す
					x1tmp64 = x1 - dcx;
					x2tmp64 = x2 - dcx;
					ytmp64  = y  - dcy;

				// 逆回転
					xxx1 = (x1tmp64 * rotate_cos_table[rr]) - (ytmp64 * rotate_sin_table[rr]);
					yyy1 = (x1tmp64 * rotate_sin_table[rr]) + (ytmp64 * rotate_cos_table[rr]);
					xxx2 = (x2tmp64 * rotate_cos_table[rr]) - (ytmp64 * rotate_sin_table[rr]);
					yyy2 = (x2tmp64 * rotate_sin_table[rr]) + (ytmp64 * rotate_cos_table[rr]);

				// 倍率
					xxx1 = ((xxx1 * 1000) / x_scale) >> 8;
					yyy1 = ((yyy1 * 1000) / y_scale) >> 8;
					xxx2 = ((xxx2 * 1000) / x_scale) >> 8;
					yyy2 = ((yyy2 * 1000) / y_scale) >> 8;

				// (scx, scy) を (0, 0) とした座標を元に戻す
					ssx1 = (int)(xxx1 + scx);
					ssy1 = (int)(yyy1 + scy);
					ssx2 = (int)(xxx2 + scx);
					ssy2 = (int)(yyy2 + scy);

				// ソース範囲でクリップ
	//				if(r == 0){		// 回転無しの場合のみ
	//					fclip(&ssx1, &ssy1, &ssx2, &ssy2, sx1, sy1, sx2, sy2);
	//				}

				// ライン式描画
					dst = dp + (((dxl * y) + x1) << 2);
					dst_size = x2 - x1 + 1;

				// 転送先のエリア補正
					if(x1 < dex1){
						x1 = dex1;
					}
					if(x2 > dex2){
						x2 = dex2;
					}

					dst_min = dp + (((dxl * y) + x1) << 2);
					dst_max = dp + (((dxl * y) + x2) << 2);

					true_color_rotate_line_draw(
						dst, dst_size, dst_min, dst_max,
						sp, sxl, syl, src_type,
						ssx1, ssy1, ssx2, ssy2,
						sx1, sy1, sx2, sy2,
						mono,
						reverse,
						c_r, c_g, c_b, c_rate,
						p_r, p_g, p_b,
						bright,
						clr,
						disp_mod
					);
				}
			}

			p1 += 2;
			p2 -= 2;
			p1_cnt--;
			p2_cnt--;
		}

	// ラインバッファ開放
		for(i=0; i<4; i++){
#ifdef __USE_MEMORY_GLOBAL_ALLOC
			if(lb_hd[i]){
				GlobalUnlock(lb_hd[i]);
				GlobalFree(lb_hd[i]);
				lb_hd[i] = 0;
				lb_p[i]  = 0;
			}
#else
			mem_free(&lb_hd[i], &lb_p[i]);
#endif
		}
	}
// ★回転なしの場合
	else{
	// Ｘパラメータ
		if(x_scale >= 0){
			ssx1 = sx1;
			ssx2 = sx2;
		}
		else{
			ssx1 = sx2;
			ssx2 = sx1;
		}

	// Ｙパラメータ
		y = dvy[0];
		if(y_scale >= 0){
			ssy1 = sy1;
		}
		else{
			ssy1 = sy2;
		}

		cnt = dvy[3] - dvy[0] + 1;
		yl = sy2 - sy1 + 1;
		alpha = 0;

		for(i=0; i<cnt; i++){

			if(y>=dey1 && y<=dey2){
				if(x_scale >= 0){
					x1 = dvx[0];
					x2 = dvx[1];
				}
				else{
					x1 = dvx[1];
					x2 = dvx[0];
				}

				if(x1<=dex2 && x2>=dex1){

				// ライン式描画
					dst = dp + (((dxl * y) + x1) << 2);
					dst_size = x2 - x1 + 1;

				// 転送先のエリア補正
					if(x1 < dex1){
						x1 = dex1;
					}
					if(x2 > dex2){
						x2 = dex2;
					}

					dst_min = dp + (((dxl * y) + x1) << 2);
					dst_max = dp + (((dxl * y) + x2) << 2);

					true_color_rotate_line_draw(
						dst, dst_size, dst_min, dst_max,
						sp, sxl, syl, src_type,
						ssx1, ssy1, ssx2, ssy1,
						sx1, sy1, sx2, sy2,
						mono,
						reverse,
						c_r, c_g, c_b, c_rate,
						p_r, p_g, p_b,
						bright,
						clr,
						disp_mod
					);
				}
			}

			alpha += yl;
			while(1){
				if(alpha < cnt){
					break;
				}
				else{
					alpha -= cnt;
					if(y_scale >= 0){
						ssy1++;
					}
					else{
						ssy1--;
					}
				}
			}
			y++;
		}
	}
